<section class="section-historia" id="section-historia">
    <div class="historia">
    <div class="wpp-img">
        
        <h1>HISTÓRIA: VANCOUVER FILM SCHOOL</h1>

        <div class="grid-layout">
  <div class="grid-item"><h2>1987</h2>
  <p>A Vancouver Film School abre suas portas pela primeira vez, para uma turma de 12 alunos de Produção Cinematográfica. </p></div>

  <div class="grid-item"><h2> 1991</h2>
  <p> A VFS apresenta o primeiro centro de treinamento de produção totalmente dedicado do mundo para novas mídias. </p></div>

  <div class="grid-item">  <h2> 1994</h2>
 <p>A VFS lança seu programa de Animação Clássica.
  A VFS inicia o primeiro programa multimídia da América do Norte. </p></div>  

  <div class="grid-item"><h2>1995 </h2>
  <p>A VFS lança seu programa de um ano para preparar os alunos para carreiras em Animação 3D e Efeitos Visuais. </p> </div>

  <div class="grid-item"><h2>1996</h2>
  <p>A Vancouver Film School entra na frente das câmeras, lançando seu programa Acting for Film & Television. </p>  </div>

  <div class="grid-item"><h2>1997</h2>
  <p>A VFS adiciona o maior laboratório digital de pintura e efeitos do Canadá ao seu programa de Animação Clássica. </p></div>  

  <div class="grid-item"><h2>1999</h2>
  <p>A VFS lança seu programa Writing for Film & Television.</p> <p>VFS abre três novas instalações: um centro de cinema expandido, uma instalação de atuação com vários estúdios e um centro de Animação Clássica realocado e expandido. </p></div>

  <div class="grid-item"><h2>2000</h2>
  <p>A VFS apresenta o Makeup Design for Film & Television como um programa de meio período. O programa tornou-se um programa de um ano em tempo integral em 2006. </p></div>

  <div class="grid-item"><h2>2002</h2>
  <p>A Vancouver Film School faz barulho! A VFS lança seu programa Sound Design for Visual Media. </p></div>

  <div class="grid-item"><h2>2003</h2>
  <p>A VFS lança os programas Foundation Visual Art & Design e Acting Essentials para apresentar aos alunos a disciplina. </p></div>

  <div class="grid-item">  <h2>2004</h2>
  <p>A VFS abre seu primeiro escritório internacional de admissões em Mumbai, Índia. A VFS lança um dos primeiros programas de design de jogos na América do Norte. </p></div>

  <div class="grid-item"> <h2>2006</h2>
  <p>Para comemorar seu 20º aniversário em 2007, a VFS oferece mais de US$ 1 milhão em bolsas de estudo para novos alunos talentosos. O canal da VFS no YouTube é lançado. </p></div>

  <div class="grid-item"> <h2>2007</h2>
  <p>A VFS lança oficialmente o VFS Blog, uma fonte sem precedentes de notícias escolares e histórias de sucesso de ex-alunos. </p></div>

  <div class="grid-item"> <h2>2008</h2>
  <p>A VFS faz parceria com o YouTube para lançar um concurso de bolsas de estudo para os criadores de três vídeos enviados e votados pela comunidade do YouTube. </p></div>

  <div class="grid-item"><h2>2013</h2>
  <p>A VFS abre seu novo Animation Campus, completo com equipamento de animação da Long Winter Studios e Qube! render farm. </p></div>

  <div class="grid-item"> <h2>2014</h2>
  <p>A VFS abre a inovadora Programação para Jogos, Web e Mobile. A VFS abre seu novo Film Campus. Em cooperação com a Universidade de Xangai, a VFS lança a Escola de Cinema Shanghai-Vancouver em Xangai. </p></div>
  
  <div class="grid-item"><h2>2015</h2>
  <p>A VFS lança seu novo site simplificado que mostra melhor a experiência do aluno e os sucessos da escola. A VFS introduz um novo programa de Arte Conceitual de Animação em seu currículo.</p></div>  
   

    <div class="grid-item"><h2>HOJE</h2>
    <p>A Vancouver Film School é líder global em educação em artes do entretenimento, oferecendo 15 programas para mais de 1.000 alunos em oito campi.</p></div>  
    </div>

