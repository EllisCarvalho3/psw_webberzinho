<section class="section-filosofia" id="section-filosofia">
    <h1>FILOSOFIA EDUCACIONAL</h1>
        <p> As chaves para o modelo educacional da VFS são a intensidade estruturada e a imersão na indústria. A Vancouver Film School foi fundada com base na filosofia de que um ano de trabalho concentrado, que equilibra a teoria liderada pela indústria e a produção prática, dá aos alunos as habilidades necessárias para criar um portfólio ou rolo de nível profissional.

        A VFS oferece 15 programas pós-secundários exclusivos - abrangendo todos os aspectos das artes do entretenimento - que são desenvolvidos e ministrados por profissionais premiados da indústria. O currículo intensivo de cada programa evolui junto com as demandas do setor e as melhores práticas atuais do setor.

        <br>
        <br>

        As experiências dos alunos emulam o mundo da indústria profissional. Os alunos estão física e mentalmente imersos nas indústrias de cinema, animação, jogos e design de Vancouver que cercam nossos campi e se beneficiam de orientação profissional e experiência de mentores, instrutores e palestrantes convidados do setor. 

        Construímos confiança. Vivemos de acordo com nossa palavra, cumprimos o que prometemos e construímos confiança com alunos, funcionários e parceiros do setor.

        Nós abraçamos a mudança. A mudança nos inspira a estar sempre criando. Nossas ferramentas são curiosidade, conhecimento e imaginação.

        Nós colaboramos. Estamos unidos no trabalho em direção a um mundo mais inclusivo. O crescimento vem do trabalho em equipe e da orientação. Celebramos a diferença, respeitamos e cuidamos uns dos outros.
    </p>

</section>

